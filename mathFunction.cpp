#include<iostream>
#include<math.h>
#include<stdlib.h>
using namespace std;
int main()
{
    cout<<"Ceil(3.7):"<<ceil(3.7)<<endl;
    cout<<"floor(3.7):"<<floor(3.7)<<endl;
    cout<<"round(3.7):"<<round(3.7)<<endl;
    cout<<"sqrt(3.7):"<<sqrt(27)<<endl;
    cout<<"pow(5,3):"<<pow(3,4)<<endl;
    cout<<"rand()%10:"<<rand()%10<<endl;
    cout<<"abs(5,3):"<<abs(-20.01)<<endl;
}